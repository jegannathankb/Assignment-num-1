let num1 = 2000;
let num2 = 1022;
let operator = "+"; 

if (operator === "+") {
  console.log("Result:", num1 + num2);
} else if (operator === "-") {
  console.log("Result:", num1 - num2);
} else if (operator === "*") {
  console.log("Result:", num1 * num2);
} else if (operator === "/") {
  console.log("Result:", num1 / num2);
} else {
  console.log("Invalid operator");
}
